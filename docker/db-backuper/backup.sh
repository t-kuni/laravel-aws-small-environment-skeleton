#!/bin/bash
FILENAME=$DB_DATABASE.sql.gz

echo DB_HOST=$DB_HOST DB_USERNAME=$DB_USERNAME DB_DATABASE=$DB_DATABASE DB_BACKUP_BUCKET_NAME=$DB_BACKUP_BUCKET_NAME

mysqldump -h $DB_HOST -u $DB_USERNAME -p$DB_PASSWORD -B $DB_DATABASE --protocol=tcp | gzip > $FILENAME \
    && aws s3 cp $FILENAME s3://$DB_BACKUP_BUCKET_NAME/
